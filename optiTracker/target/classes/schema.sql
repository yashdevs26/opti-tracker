CREATE TABLE `USER_INFO` (
  `USER_ID` int NOT NULL AUTO_INCREMENT,
  `USER_NAME` varchar(50) DEFAULT '',
  `FULL_NAME` varchar(200) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci DEFAULT '',
  `EMAIL` varchar(120) NOT NULL DEFAULT '',
  `USER_KEY` varchar(60) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL DEFAULT '',
  `STATUS` varchar(20) DEFAULT '',
  `EMAIL_TOKEN` varchar(60) CHARACTER SET utf8mb4 COLLATE utf8mb4_0900_ai_ci NOT NULL,
  PRIMARY KEY (`USER_ID`)
) ENGINE=InnoDB AUTO_INCREMENT=11 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;

CREATE TABLE `TASK_INFO` (
  `TASK_ID` int NOT NULL AUTO_INCREMENT,
  `USER_ID` int NOT NULL,
  `DESCRIPTION` varchar(255) DEFAULT '',
  `STATUS` varchar(20) DEFAULT '',
  `START_TIME` datetime DEFAULT NULL,
  `TARGET_TIME` datetime DEFAULT NULL,
  `END_TIME` datetime DEFAULT NULL,
  `CATEGORY` varchar(100) DEFAULT '',
  PRIMARY KEY (`TASK_ID`),
  KEY `USER_ID` (`USER_ID`),
  CONSTRAINT `TASK_INFO_ibfk_1` FOREIGN KEY (`USER_ID`) REFERENCES `USER_INFO` (`USER_ID`)
) ENGINE=InnoDB AUTO_INCREMENT=5 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci; 
